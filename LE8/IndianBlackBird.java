public class IndianBlackBird implements BlackBird {
    @Override
    public void scream() {
        System.out.println("Indian black bird screams loudly");
    }

    @Override
    public void glide() {
        System.out.println("Indian black bird glides through the sky");
    }

    @Override
    public void munch() {
        System.out.println("Indian black bird munches on grains");
    }
}
